How to Create Wi-Fi Hotspot on Windows OS ?<br>
Answer: Sample as it is ! Just follow the Instractions.<br>
1. Go to the Start Menu and Run the Command Prompt.exe as Administrator.<br>

2. At first need to check weither your machine has the ability to Hosted network and for this write below command
"netsh wlan show drivers". press Enter.<br>
3. If you find Hosted network supported: yes ; Then only you can create wifi-hotspot.<br>

4. Now for creating Connection write below command:
" netsh wlan set hostednetwork mode=allow ssid=Hostpot_Name key=Password(8-32characters) ". press Enter.<br>

5. Now for start connection write below command:
" netsh wlan start hostednetwork ". press Enter.<br>

6. If connection gets started succesfully you will get one success note
"hosted network started".<br>

Now exit the Command Prompt.exe<br>

7. Now Open Network and Sharing Center.<br>

8. Now you will see two types of network 1.With Accesss type: Internet 2.With Access type: No internet access<br>

9. Open the properties of 1 and go to the sharing tab.<br>

10. In Home networking connection select type 2 connection.<br>

11. Check all checkbox.<br>

12. Press OK and exit.<br>

Thats it. Now your Hotspot started.<br>

Note: After every Reboot for starting Hotspot follow the followings:<br>

1. Run Command Promt.exe as Administrator and type below command:
" netsh wlan start hostednetwork ". press Enter.<br>

2. Thats it. Hotspot start.<br>
